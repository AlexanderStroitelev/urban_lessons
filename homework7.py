# создание множество из разных данные с повторением
my_set = [5, 7, 5, 'abs', (1,2,3,4), 9, 8, 5, 'abs', 9, 7, 4, 1, 1]

# создание уникального по значениям множества и вывод его на экран
print(set(my_set))

# добаляем новые элементы в множество
my_set.append(10)
my_set.append(25)

# удалям один из элементы списка
my_set.remove((1,2,3,4))

# выводим на экран изменное множество
print(my_set)