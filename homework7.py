# создание словаря
my_dict = {
    'рука': 'hand',
    'голова': 'hear',
    'нога': 'leg',
    'рост': '200',
    'вес': '100'
    }

# выводим созданный словарь
print(my_dict)

# печатаем значение в ключе 'рост' словаря
print(my_dict['рост'])

# печатаем отсутствующее значение в словаре без ошибки
print(my_dict.get('страна'))

# добавили новую пару ключ:значение в словарь
my_dict['имя'] = 'Александр'

# добавили новую пару ключ:значение в словарь
my_dict['место рождения'] = 'Волжск'

# удалили одну из пар по ключу в словаре и присвоили переменной значение удаленного ключа
# и вывели на экран значение удаленного ключа
deleted_value = my_dict.pop('нога')
print (deleted_value)

# вывод обработанного словааря
print(my_dict)

